/**
 * 从词库中选取未被学过的单词。
 * @param learnedWords 今日已学的单词文本集合
 * @param count 需要选取的数量
 */
export function pickWords(learnedWords: Set<string>, count: number): Word[] {
  // 去重：保留每个单词第一次出现的条目（即数据更完整的版本）
  const seen = new Set<string>()
  const unique = WORD_BANK.filter((w) => {
    if (seen.has(w.word)) return false
    seen.add(w.word)
    return true
  })
  const available = unique.filter((w) => !learnedWords.has(w.word))
  // Fisher-Yates 洗牌
  for (let i = available.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[available[i], available[j]] = [available[j], available[i]]
  }
  return available.slice(0, Math.min(count, available.length))
}
